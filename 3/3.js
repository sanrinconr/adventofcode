//A partir de un \n se divide un string para retornar un arreglo con las filas separadas
function obtenerFilas(str){
    //En caso de que no tenga un salto la ultima linea
    str+="\n";
    var arregloPalabras = []
    var actual=""
    for (let i = 0; i < str.length; i++) {
       if (str[i] !== "\n") {
           actual+=(str[i])
       } else{
           arregloPalabras.push(actual)
           actual = ""
       }
    }
    return arregloPalabras
}
function setCharAt(str,index,chr) {
    if(index > str.length-1) return str;
    return str.substring(0,index) + chr + str.substring(index+1);
}
